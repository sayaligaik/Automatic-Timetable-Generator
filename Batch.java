package com.model;

/**
 *
 * @author Ganesh
 */
public class Batch {
    int batchID;
    String batchName;
    String batchAddDate;
    String status;

    public int getBatchID() {
        return batchID;
    }

    public void setBatchID(int batchID) {
        this.batchID = batchID;
    }

    public String getBatchName() {
        return batchName;
    }

    public void setBatchName(String batchName) {
        this.batchName = batchName;
    }

    public String getBatchAddDate() {
        return batchAddDate;
    }

    public void setBatchAddDate(String batchAddDate) {
        this.batchAddDate = batchAddDate;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
}
