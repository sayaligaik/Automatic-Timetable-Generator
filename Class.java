package com.model;

/**
 *
 * @author Ganesh
 */
public class Class {
    int classId;
    String className;
    String classAddDate;
    int status;

    public int getClassId() {
        return classId;
    }

    public void setClassId(int classId) {
        this.classId = classId;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getClassAddDate() {
        return classAddDate;
    }

    public void setClassAddDate(String classAddDate) {
        this.classAddDate = classAddDate;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
    
}
