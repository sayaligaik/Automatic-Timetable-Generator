package com.model;

/**
 *
 * @author Ganesh
 */
public class Time {
    int timeId;
    String timeBlock;
    int timeReason;

    public int getTimeId() {
        return timeId;
    }

    public void setTimeId(int timeId) {
        this.timeId = timeId;
    }

    public String getTimeBlock() {
        return timeBlock;
    }

    public void setTimeBlock(String timeBlock) {
        this.timeBlock = timeBlock;
    }

    public int getTimeReason() {
        return timeReason;
    }

    public void setTimeReason(int timeReason) {
        this.timeReason = timeReason;
    }
    
    
}
