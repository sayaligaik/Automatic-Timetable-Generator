package com.model;

/**
 *
 * @author Ganesh
 */
public class Lab {
    int labId;
    String labName;
    String labAddDate;
    String status;

    public int getLabId() {
        return labId;
    }

    public void setLabId(int labId) {
        this.labId = labId;
    }

    public String getLabName() {
        return labName;
    }

    public void setLabName(String labName) {
        this.labName = labName;
    }

    public String getLabAddDate() {
        return labAddDate;
    }

    public void setLabAddDate(String labAddDate) {
        this.labAddDate = labAddDate;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
}
